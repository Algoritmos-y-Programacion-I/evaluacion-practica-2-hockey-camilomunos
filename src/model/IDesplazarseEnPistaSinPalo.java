package model;

/**
 * Interface que define los métodos para entidades que se desplazan en la pista sin palo.
 */
public interface IDesplazarseEnPistaSinPalo {

    /**
     * Permite que la entidad se desplace en la pista sin palo.
     * @return Una cadena que describe cómo la entidad se desplaza.
     */
    String desplazarse();
}
